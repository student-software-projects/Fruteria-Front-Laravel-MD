       <h4>MISIÓN</h4>
       <p>
           Surtifruver de la Sabana es una Empresa Colombiana dedicada a la comercialización de alimentos agropecuarios de alta calidad y la más amplia variedad de productos saludables. Nuestro compromiso es con la excelencia en el servicio, bienestar y total satisfacción de los clientes y aliados estratégicos.
       </p>
       <h4>VISIÓN</h4>
       <p>
           Surtifruver de la Sabana en 2020 se consolidará en el mercado como la mejor opción de compra de alimentos del agro, con presencia a nivel nacional y con canales directos e indirectos de comercialización
       </p>
       <h4>FUNDADOR</h4>
       <p>
           Alonso Orjuela Pardo nació en La Plata, Huila, hace 46 años. Según lo contó varias veces, comenzó su emporio de negocio de frutas, que actualmente tiene una participación muy importante en el mercado de frutas y verduras en el centro el país, arrendando un espacio muy pequeño dentro del supermercado de un tío en el municipio de Chía, al norte de Bogotá, donde empezó trabajando como surtidor y domiciliario.
           De niño creció en una finca, que años más tarde sus padres tuvieron que abandonar a consecuencia de la violencia que se vivía en la región.  Vendió una bicicleta y una cadena de oro, con ese dinero empezó su negocio en el local de su tío Parmenio. Hoy, 30 años después, esta vivo su legado en múltiples  establecimientos que llamó Surtifruver, en los que se venden frutas y verduras traídas directamente del campo.
           El slogan de Alonso y que se vive diariamente en Surtifruver ha sido desde un principio, es y siempre sera: " Lo nuestro es lo natural"
       </p>
